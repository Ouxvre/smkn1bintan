        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });

        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -100px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, observerOptions);

        document.querySelectorAll('.card, .timeline-item, .stat-item, .skill-item').forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateY(30px)';
            el.style.transition = 'all 0.6s ease';
            observer.observe(el);
        });

        document.querySelectorAll('.gallery-item').forEach((item, index) => {
            item.style.opacity = '0';
            item.style.transform = 'scale(0.8) rotate(-5deg)';
            item.style.transition = 'all 0.6s cubic-bezier(0.34, 1.56, 0.64, 1)';
            setTimeout(() => {
                observer.observe(item);
            }, index * 100);
        });

        const stats = document.querySelectorAll('.stat-number');
        const animateStats = (entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting && !entry.target.classList.contains('animated')) {
                    entry.target.classList.add('animated');
                    const target = entry.target;
                    const text = target.textContent;
                    const isNumber = !isNaN(parseInt(text));
                    
                    if (isNumber) {
                        const finalNumber = parseInt(text);
                        let current = 0;
                        const increment = finalNumber / 60;
                        const timer = setInterval(() => {
                            current += increment;
                            if (current >= finalNumber) {
                                target.textContent = finalNumber + (text.includes('%') ? '%' : text.includes('+') ? '+' : '');
                                clearInterval(timer);
                            } else {
                                target.textContent = Math.floor(current) + (text.includes('%') ? '%' : text.includes('+') ? '+' : '');
                            }
                        }, 25);
                    }
                }
            });
        };

        const statObserver = new IntersectionObserver(animateStats, {
            threshold: 0.5
        });

        stats.forEach(stat => {
            statObserver.observe(stat);
        });

        const lightnings = document.querySelectorAll('.lightning');
        setInterval(() => {
            const randomLightning = lightnings[Math.floor(Math.random() * lightnings.length)];
            randomLightning.style.animation = 'none';
            setTimeout(() => {
                randomLightning.style.animation = '';
            }, 10);
        }, 3000);
    </script>

